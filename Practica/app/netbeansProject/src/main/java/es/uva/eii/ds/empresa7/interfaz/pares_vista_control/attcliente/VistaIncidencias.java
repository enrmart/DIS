    private void configuraPantalla() {
        // Obtener el tamaÃ±o de la pantalla
        Dimension pantallaTamano = Toolkit.getDefaultToolkit().getScreenSize();

        // Obtener el tamaÃ±o del componente
        Dimension componenteTamano = getPreferredSize();

        // Calcular la coordenada x para que el componente estÃ© en el centro de la pantalla
        int x = (pantallaTamano.width - componenteTamano.width) / 2;

        // Calcular la coordenada y par a que el componente estÃ© en el centro de la pantalla
        int y = (pantallaTamano.height - componenteTamano.height) / 2;

        // Establecer la ubicaciÃ³n y el tamaÃ±o del componente
        setBounds(x, y, componenteTamano.width, componenteTamano.height);
        setSize(670,490);
    }
